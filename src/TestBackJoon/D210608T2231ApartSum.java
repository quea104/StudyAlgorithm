package TestBackJoon;
/*
 * 문제명: 블랙잭
 * 일자: 21.06.08.화
 * https://www.acmicpc.net/problem/2231
 * 문제내용:
 		어떤 자연수 N이 있을 때, 그 자연수 N의 분해합은 N과 N을 이루는 각 자리수의 합을 의미한다. 어떤 자연수 M의 분해합이 N인 경우, M을 N의 생성자라 한다. 예를 들어, 245의 분해합은 256(=245+2+4+5)이 된다. 
 		따라서 245는 256의 생성자가 된다. 물론, 어떤 자연수의 경우에는 생성자가 없을 수도 있다. 반대로, 생성자가 여러 개인 자연수도 있을 수 있다.
		자연수 N이 주어졌을 때, N의 가장 작은 생성자를 구해내는 프로그램을 작성하시오.
 * 입력: 첫째 줄에 자연수 N(1 ≤ N ≤ 1,000,000)이 주어진다.
 * 출력: 첫째 줄에 답을 출력한다. 생성자가 없는 경우에는 0을 출력한다.
 * 풀이: https://st-lab.tistory.com/98
 		최소값이 N-9*N자리수인데 탐색할 수 있는 숫자 범위의 가장 작은 값을 구해야 하므로 생성자의 가장 큰 값(N+9*N자리수)을 역으로 생각하여 9*N자리수룰 빼줘야 함
 */

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class D210608T2231ApartSum {
	public static void main(String[] args) throws IOException {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		String str_N = br.readLine();
		int len = str_N.length();
		int N = Integer.parseInt(str_N);
		int result = 0;
		for(int i = N-(9*len); i < N; i++) {
			int sum = i;
			int temp = i;
			while(temp > 0) {
				sum += temp%10;
				temp /= 10;
			}
				
			if(sum == N) {
				result = i;
				break;
			}
		}
		System.out.println(result);
	}
}
